public class Program_VII_4_PatternF {
    public static void main(String[] args) {
    for(int i=5;i>=1;i--)
    {
        for(int j=i;j>=1;j--)
        {
            System.out.print(j+" ");
        }
        System.out.println();
        }
        
        for(int i=2;i<=5;i++)
        {
            for(int j=i;j>=1;j--)
            {
                System.out.print(j+" ");
            }
            System.out.println();
        }
    
    }
}
